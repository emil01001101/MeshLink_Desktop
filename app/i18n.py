# ---------------------------------------------------------------------------
# Singleton i18n
# ---------------------------------------------------------------------------
class _I18N(QObject):
    languageChanged = Signal(str)

    def __init__(self):
        super().__init__()
        self._lang = "en"

    @property
    def current(self) -> str:
        return self._lang

    def set_language(self, lang: str):
        if lang not in TRANSLATIONS:
            lang = "en"
        if lang != self._lang:
            self._lang = lang
            self.languageChanged.emit(lang)

    def t(self, key: str, *args) -> str:
        """Traduce o cheie. Daca lipseste, returneaza cheia."""
        d = TRANSLATIONS.get(self._lang, {})
        s = d.get(key)
        if s is None:
            s = TRANSLATIONS.get("en", {}).get(key, key)
        if args:
            try:
                return s.format(*args)
            except Exception:
                return s
        return s


# instanta globala
i18n = _I18N()


def t(key: str, *args) -> str:
    """Shortcut pentru i18n.t()."""
    return i18n.t(key, *args)
